#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	t_list	*tmp;

	tmp = *lst;
	*lst = new;
	if (!new)
		return ;
	while (new->next)
		new = new->next;
	new->next = tmp;
}
